
def NumberCheck(a):   
    
    if a > 0:   
        print("Number given by you is Positive")   
       
    elif a < 0:   
        print("Number given by you is Negative")   
    
    else:   
        print("Number given by you is zero")  

a = float(input("Enter a number as input value: "))  
  
NumberCheck(a)